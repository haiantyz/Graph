public class BoudaryReturn {
    private int first,second,third,fourth;
    public BoudaryReturn(int first,int second,int third,int fourth){
        this.first = first;
        this.second = second;
        this.third = third;
        this.fourth = fourth;
    }


    public int getFirst() {
        return first;
    }

    public void setFirst(int first) {
        this.first = first;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }

    public int getThird() {
        return third;
    }

    public void setThird(int third) {
        this.third = third;
    }

    public int getFourth() {
        return fourth;
    }

    public void setFourth(int fourth) {
        this.fourth = fourth;
    }
}
